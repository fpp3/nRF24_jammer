// Bitmap container structure for both static and animated bitmaps
typedef struct {
  const uint8_t **frames; // Pointer to array of frame pointers (single frame for static)
  const uint8_t width;
  const uint8_t height;
  const uint8_t frameCount;       // 1 for static bitmaps, >1 for animated
  const uint16_t frameDurationMs; // Time per frame in milliseconds (0 for static)
} bitmap_s;

